// Print macro
#define print(text, level)		std::cout << ((level == 0)? "" : "[OTHER] ") << text << std::endl;
#define print(text)				std::cout << text << std::endl;
// Prints macro, without new line
#define prints(text, level)		std::cout << ((level == 0)? "" : "[OTHER] ") << text;
#define prints(text)			std::cout << text;

